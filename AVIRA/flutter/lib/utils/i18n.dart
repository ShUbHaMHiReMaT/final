  // ── Public API ───────────────────────────────────────────────────────────

  /// Returns the list of supported language descriptors, each with 'code' and 'name'.
  static List<Map<String, String>> get supportedLanguages =>
      List.unmodifiable(_supportedLanguages);

  /// Sets the active language. Falls back to 'en' if [langCode] is unsupported.
  static void setLanguage(String langCode) {
    if (translations.containsKey(langCode)) {
      currentLang = langCode;
    } else {
      currentLang = 'en';
    }
  }

  /// Looks up [key] in the current language table.
  /// Returns the English fallback if key is missing from the target language.
  /// Returns [key] itself if not found in any language.
  static String t(String key) {
    // 1. Try current language
    final inCurrent = translations[currentLang]?[key];
    if (inCurrent != null && inCurrent.isNotEmpty) return inCurrent;

    // 2. Fallback to English
    final inEnglish = translations['en']?[key];
    if (inEnglish != null && inEnglish.isNotEmpty) return inEnglish;

    // 3. Return raw key as last resort
    return key;
  }

  /// Returns the native display name for a given language code.
  /// E.g. 'kn' → 'ಕನ್ನಡ'
  static String displayName(String langCode) {
    for (final lang in _supportedLanguages) {
      if (lang['code'] == langCode) return lang['name'] ?? langCode;
    }
    return langCode;
  }

  /// Returns true if [langCode] is a supported language.
  static bool isSupported(String langCode) =>
      translations.containsKey(langCode);

  /// Returns all keys defined in the English base translation.
  static List<String> get allKeys =>
      translations['en']?.keys.toList() ?? const [];
